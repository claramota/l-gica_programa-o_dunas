#adição
numero1= 10
numero2= 20
soma = numero1 + numero2 
print(soma)

#subtração 
subtracao = numero1 - numero2
print(subtracao)

#multiplcação 
multiplicacao = numero1 * numero2
print(multiplicacao)

#divisão
divisao = numero1 / numero2
print(divisao)

#potência
potencia = numero1 ** numero2
print(potencia)

#modulo
modulo = numero1 % numero2
print(modulo)

#resto da divisão 
restodadivisao = numero1 // numero2
print(restodadivisao)